// A data structure that allows to store a collection of elements of the same type , under a single variable name
 class demo {
    public static void main(String args[])
    {
        String[] song = new String[3];
        song[0]="s1";
        song[1]="s2";
        song[2]="s3";

        System.out.println(song[2]);
    }
 }